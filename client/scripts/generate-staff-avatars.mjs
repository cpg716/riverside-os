/**
 * Generates bundled staff avatar SVGs (DiceBear, MIT-licensed styles).
 * Run from repo: cd client && npm run generate:staff-avatars
 */
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createAvatar } from "@dicebear/core";
import * as lorelei from "@dicebear/lorelei";
import * as avataaars from "@dicebear/avataaars";
import * as adventurer from "@dicebear/adventurer";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const publicDir = path.join(__dirname, "../public/staff-avatars");

/** @type {{ key: string, style: typeof lorelei, seed: string }[]} */
const entries = [{ key: "ros_default", style: lorelei, seed: "riverside-neutral-portrait" }];

const loreleiSeeds = [
  "Amara", "ChenWei", "Diego", "Emi", "Fatima", "Giovanni", "Hana", "Ivan", "Jamal", "Keiko",
  "Luisa", "Marcus", "Nia", "Omar", "Priya", "QuinnR", "Rosa", "Samir", "Tara", "Viktor",
  "Yuki", "Zara", "Aaliyah", "Bruno", "Chloe", "Dmitri", "Elena", "Frank",
];
loreleiSeeds.forEach((seed, i) => {
  entries.push({ key: `lorelei_${String(i + 1).padStart(2, "0")}`, style: lorelei, seed });
});

const avaSeeds = [
  "Skyler", "Jordan", "Riley", "Casey", "Morgan", "Avery", "Quinn", "Reese", "Parker", "Drew",
  "Blake", "Cameron", "Sage", "River", "Phoenix", "Rowan", "Emery", "Finley", "Hayden", "Kendall",
  "Logan", "Marley",
];
avaSeeds.forEach((seed, i) => {
  entries.push({ key: `avataaars_${String(i + 1).padStart(2, "0")}`, style: avataaars, seed });
});

const advSeeds = [
  "Mimi", "Sasha", "Gracie", "Aneka", "Jocelyn", "Vivian", "Liliana", "Natalia", "Beth", "Jade",
  "Eden", "Harper", "Willow", "Ruby", "Iris", "Luna", "Stella", "Nova", "Maya", "Zoe",
  "Nina", "Cleo",
];
advSeeds.forEach((seed, i) => {
  entries.push({ key: `adventurer_${String(i + 1).padStart(2, "0")}`, style: adventurer, seed });
});

mkdirSync(publicDir, { recursive: true });

for (const { key, style, seed } of entries) {
  const svg = createAvatar(style, { seed }).toString();
  writeFileSync(path.join(publicDir, `${key}.svg`), svg, "utf8");
}

const keys = entries.map((e) => e.key);
const tsPath = path.join(__dirname, "../src/lib/staffAvatarCatalog.generated.ts");
writeFileSync(
  tsPath,
  `// @generated by scripts/generate-staff-avatars.mjs — do not edit by hand
export const STAFF_AVATAR_KEYS = ${JSON.stringify(keys)} as const;

export type StaffAvatarKey = (typeof STAFF_AVATAR_KEYS)[number];

export type StaffAvatarGroup = "default" | "lorelei" | "avataaars" | "adventurer";

export interface StaffAvatarCatalogEntry {
  key: StaffAvatarKey;
  group: StaffAvatarGroup;
}

export const STAFF_AVATAR_CATALOG: StaffAvatarCatalogEntry[] = ${JSON.stringify(
    entries.map((e) => {
      let group = "lorelei";
      if (e.key === "ros_default") group = "default";
      else if (e.key.startsWith("avataaars_")) group = "avataaars";
      else if (e.key.startsWith("adventurer_")) group = "adventurer";
      return { key: e.key, group };
    }),
    null,
    2,
  )};
`,
  "utf8",
);

const rustIncPath = path.join(__dirname, "../../server/src/auth/staff_avatar_allowlist.inc");
const rustBody = `// @generated by client/scripts/generate-staff-avatars.mjs
pub const ALLOWED_STAFF_AVATAR_KEYS: &[&str] = &[
${keys.map((k) => `    "${k}",`).join("\n")}
];
`;
writeFileSync(rustIncPath, rustBody, "utf8");

console.log(`Wrote ${keys.length} SVGs to public/staff-avatars/`);
console.log(`Wrote ${tsPath}`);
console.log(`Wrote ${rustIncPath}`);
